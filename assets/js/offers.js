/**
 * offers.js — Special Offers page.
 *
 *   1. Load best calculated offers (highest discount percentage).
 *   2. Load admin-curated featured offers.
 *   3. For admins: show "Replace Product" button to swap featured products.
 */
(function () {
    'use strict';

    const { esc, buildPath, toast } = window.UI;
    const Api = window.CustomerApi;

    let currentReplaceOfferId = null;

    /**
     * Verify admin session from URL parameter (for admin preview from customer views).
     * This is secure because:
     * 1. The session key is only valid for 5 minutes
     * 2. It was generated by the authenticated admin panel
     * 3. It uses a hash, not the full token
     */
    function verifyAdminSession() {
        try {
            const urlParams = new URLSearchParams(window.location.search);
            const sessionKey = urlParams.get('_admin_session');
            if (!sessionKey) return false;

            const storedKey = sessionStorage.getItem('admin_session_key');
            const storedTime = sessionStorage.getItem('admin_session_time');

            if (!storedKey || !storedTime) return false;
            if (storedKey !== sessionKey) return false;

            // Check if session expired (5 minutes = 300000ms)
            const timestamp = parseInt(storedTime, 10);
            if (Date.now() - timestamp > 300000) {
                // Clean up expired session
                sessionStorage.removeItem('admin_session_key');
                sessionStorage.removeItem('admin_session_time');
                return false;
            }

            return true;
        } catch (e) {
            return false;
        }
    }

    /**
     * Check if current user is an admin.
     * Looks for admin token in localStorage OR a valid admin session from URL.
     */
    function isAdmin() {
        // Check 1: Direct admin login (adminToken in localStorage)
        const adminToken = localStorage.getItem('adminToken');
        if (adminToken) return true;

        // Check 2: Admin session from "View as Admin" link
        if (verifyAdminSession()) return true;

        return false;
    }

    /**
     * Format price with currency.
     */
    function fmtPrice(value) {
        return '$' + Number(value || 0).toFixed(2);
    }

    /**
     * Calculate discount percentage.
     */
    function calcDiscount(price, discountPrice) {
        if (!discountPrice || discountPrice <= 0 || discountPrice >= price) {
            return 0;
        }
        return Math.round(((price - discountPrice) / price) * 100);
    }

    /**
     * Create offer card HTML.
     */
    function offerCard(product, isFeatured = false, offerId = null) {
        const discount = calcDiscount(product.price, product.discount_price);
        const savings = product.price - (product.discount_price || product.price);

        const img = product.image_url
            ? `<img src="${esc(product.image_url)}" alt="${esc(product.name)}" loading="lazy">`
            : `<div style="display:grid;place-items:center;height:100%;color:var(--text-muted);">No image</div>`;

        const discountBadge = discount > 0
            ? `<div class="offer-card__discount-badge">-${discount}%</div>`
            : '';

        const originalPrice = discount > 0
            ? `<span class="offer-card__original">${fmtPrice(product.price)}</span>`
            : '';

        const savingsText = savings > 0
            ? `<div class="offer-card__savings">You save ${fmtPrice(savings)}</div>`
            : '';

        const replaceBtn = isFeatured && isAdmin()
            ? `<button type="button" class="admin-replace-btn" data-replace-offer="${esc(String(offerId))}" data-product-name="${esc(product.name)}">
                Replace Product
            </button>`
            : '';

        return `
            <div class="offer-card" data-product-id="${esc(String(product.id))}">
                <a href="${esc(buildPath('/views/customer/product.php?id=' + encodeURIComponent(product.id)))}" style="display:block;text-decoration:none;color:inherit;">
                    <div class="offer-card__image">
                        ${discountBadge}
                        ${img}
                    </div>
                    <div class="offer-card__body">
                        <h3 class="offer-card__name">${esc(product.name)}</h3>
                        <div class="offer-card__prices">
                            <span class="offer-card__price">${fmtPrice(product.discount_price || product.price)}</span>
                            ${originalPrice}
                        </div>
                        ${savingsText}
                    </div>
                </a>
                ${replaceBtn}
            </div>
        `;
    }

    /**
     * Load best calculated offers.
     */
    async function loadBestOffers() {
        const container = document.querySelector('[data-offers-best]');
        if (!container) return;

        try {
            const response = await Api.get('/offers/best', { query: { limit: 12 } });
            const offers = Array.isArray(response) ? response : (response.data || []);

            if (!offers.length) {
                // Fallback: load regular products if no discounts available
                await loadRegularProductsAsFallback(container);
                return;
            }

            container.innerHTML = offers.map(p => offerCard(p)).join('');
        } catch (e) {
            container.innerHTML = '<div class="no-results">Failed to load offers. Please try again later.</div>';
            console.error('Failed to load best offers:', e);
        }
    }

    /**
     * Fallback: Load regular products when no discounts exist.
     */
    async function loadRegularProductsAsFallback(container) {
        try {
            const response = await Api.get('/products', { query: { limit: 12, page: 1 } });
            const products = response.items || [];

            if (!products.length) {
                container.innerHTML = '<div class="no-results">No products available yet.</div>';
                return;
            }

            container.innerHTML = `
                <div class="no-results" style="grid-column:1/-1;text-align:left;background:#fff5e6;padding:16px;border-radius:8px;margin-bottom:16px;">
                    <strong>No discounted products yet.</strong> Showing regular products instead.
                    <br><small>Admins can add discount prices to products in the admin panel.</small>
                </div>
                ${products.map(p => offerCard(p)).join('')}
            `;
        } catch (e) {
            container.innerHTML = '<div class="no-results">Failed to load products. Please try again later.</div>';
        }
    }

    /**
     * Load admin-curated featured offers.
     */
    async function loadFeaturedOffers() {
        const container = document.querySelector('[data-offers-featured]');
        if (!container) return;

        try {
            const response = await Api.get('/offers/featured', { query: { limit: 12 } });
            const offers = Array.isArray(response) ? response : (response.data || []);

            if (!offers.length) {
                container.innerHTML = '<div class="no-results">No featured offers yet.</div>';
                return;
            }

            container.innerHTML = offers.map(o => {
                const product = o.product;
                if (!product) return '';
                return offerCard(product, true, o.id);
            }).join('');

            // Wire up replace buttons
            if (isAdmin()) {
                wireReplaceButtons();
            }
        } catch (e) {
            container.innerHTML = '<div class="no-results">Failed to load featured offers.</div>';
            console.error('Failed to load featured offers:', e);
        }
    }

    /**
     * Wire up the "Replace Product" buttons for admin.
     */
    function wireReplaceButtons() {
        document.querySelectorAll('[data-replace-offer]').forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                const offerId = btn.dataset.replaceOffer;
                const productName = btn.dataset.productName;
                openReplaceModal(offerId, productName);
            });
        });
    }

    /**
     * Open the replace product modal.
     * Uses inline style for display because the `hidden` attribute
     * can be overridden by inline styles in some browsers.
     */
    function openReplaceModal(offerId, productName) {
        currentReplaceOfferId = offerId;
        const modal = document.querySelector('[data-replace-modal]');
        const input = document.getElementById('replaceProductId');

        if (modal) {
            modal.style.display = 'flex';
            if (input) {
                input.value = '';
                input.placeholder = `Replace "${productName}" with Product ID...`;
                setTimeout(() => input.focus(), 50);
            }
        }
    }

    /**
     * Close the replace modal.
     */
    function closeReplaceModal() {
        const modal = document.querySelector('[data-replace-modal]');
        if (modal) {
            modal.style.display = 'none';
        }
        currentReplaceOfferId = null;
    }

    /**
     * Handle product replacement.
     * Uses the admin token from localStorage (only available when admin is signed in).
     * Same-origin localStorage IS shared between admin and customer views.
     */
    async function handleReplace() {
        if (!currentReplaceOfferId) {
            alert('No offer selected to replace');
            return;
        }

        const input = document.getElementById('replaceProductId');
        const newProductId = input.value.trim();

        if (!newProductId) {
            alert('Please enter a product ID');
            input.focus();
            return;
        }

        const adminToken = localStorage.getItem('adminToken');
        if (!adminToken) {
            alert('You must be signed in as an admin to replace products.\nPlease open this page from the admin panel.');
            return;
        }

        if (!confirm(`Replace this product with Product ID ${newProductId}?`)) {
            return;
        }

        try {
            const response = await fetch(
                `${window.APP_CONFIG.apiBase}/admin/featured-offers/${currentReplaceOfferId}/replace`,
                {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'Accept': 'application/json',
                        'Authorization': `Bearer ${adminToken}`,
                    },
                    body: JSON.stringify({ new_product_id: parseInt(newProductId, 10) }),
                }
            );

            const data = await response.json();

            if (!response.ok) {
                const errMsg = data.message || data.error
                    || (data.errors ? Object.values(data.errors)[0]?.[0] : null)
                    || 'Failed to replace product';
                throw new Error(errMsg);
            }

            toast('Product replaced successfully');
            closeReplaceModal();
            loadFeaturedOffers(); // Refresh the list
        } catch (e) {
            alert('Failed to replace product: ' + e.message);
            console.error('Replace failed:', e);
        }
    }

    /**
     * Bind modal event listeners.
     * Called BEFORE async loading so buttons work immediately.
     */
    function bindModalEvents() {
        const cancelBtn = document.querySelector('[data-close-replace]');
        if (cancelBtn) {
            cancelBtn.addEventListener('click', closeReplaceModal);
        }

        const confirmBtn = document.getElementById('confirmReplace');
        if (confirmBtn) {
            confirmBtn.addEventListener('click', handleReplace);
        }

        // Close modal on backdrop click (clicking the outer overlay)
        const modal = document.querySelector('[data-replace-modal]');
        if (modal) {
            modal.addEventListener('click', (e) => {
                if (e.target === modal) {
                    closeReplaceModal();
                }
            });
        }

        // Close modal on Escape key
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && currentReplaceOfferId) {
                closeReplaceModal();
            }
        });

        // Enter key on input submits replace
        const input = document.getElementById('replaceProductId');
        if (input) {
            input.addEventListener('keydown', (e) => {
                if (e.key === 'Enter') {
                    e.preventDefault();
                    handleReplace();
                }
            });
        }
    }

    /**
     * Initialize the offers page.
     */
    async function init() {
        // Wire up modal buttons FIRST so they always work
        bindModalEvents();

        // Then load both sections in parallel
        await Promise.all([
            loadBestOffers(),
            loadFeaturedOffers(),
        ]);
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
})();
